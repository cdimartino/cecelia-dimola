      <h2>Carrot Cake</h2>

      <ul>
        <li>1 1/2 cup of vegetable oil</li>
        <li>2 cups sugar</li>
        <li>4 eggs</li>
        <li>2 cups flour</li>
        <li>2 tsp baking soda</li>
        <li>1 tsp salt</li>
        <li>1 tbsp cinnamon</li>
        <li>1 tbsp vanilla</li>
        <li>1 cup coconut</li>
        <li>3 cups grated carrots</li>
      </ul>

